import {ethers} from "ethers";
import maskUtil from "../utils/maskUtil.js";
import {ContractAddressConfig} from "../config/contractAddressConfig.js";

const erc20ContractApi = {
    contract: null,
    async getContract() {
        const single = await maskUtil.getSingle()
        if (single) {
            this.contract = new ethers.Contract(ContractAddressConfig.ERC20, ABI, single)
        }
        else {
            this.contract = new ethers.Contract(ContractAddressConfig.ERC20, ABI, maskUtil.getProvider())
        }
        return this.contract
    },
    async balanceOf(address) {
        if (address === null) return 0
        return (await this.getContract()).balanceOf(address)
    },
    async getCheckinReward(oldCheckinStreak) {
        return (await this.getContract()).getCheckinReward(oldCheckinStreak)
    },
    async checkin() {
        console.log((await this.getContract()))
        const tx = await (await this.getContract()).checkin();
        const receipt = await tx.wait();
        return receipt.status === 1
    },
    async checkinDate(address) {
        if (address === null) return 0
        return (await this.getContract()).checkinDate(address)
    },
    async checkinStreak(address) {
        return (await this.getContract()).checkinStreak(address)
    }
}

export const erc20DrawContractApi = {
    async getContract() {
        return await erc20ContractApi.getContract()
    },
    async getDrawCount(address) {
        return (await this.getContract()).luckyDrawCount(address)
    },
    async luckyDraw() {
        const contract = await this.getContract();

        // 1. 发送交易
        const tx = await contract.luckyDraw(0, { gasLimit: 200000 });

        // 2. 等待交易确认，拿到 receipt
        const receipt = await tx.wait();

        // 3. 解析日志
        const iface = contract.interface;
        for (const log of receipt.logs) {
            try {
                const parsed = iface.parseLog(log);
                if (parsed && parsed.name === "LuckyDraw") {
                    return parsed.args.reward;
                }
            } catch (e) {
                // 不是本合约的日志就忽略
            }
        }

        return null; // 如果没找到事件
    },
}

export default erc20ContractApi

