import maskUtil from "../utils/maskUtil.js";
import {ContractAddressConfig} from "../config/contractAddressConfig.js";
import {ethers} from "ethers";

const nftContractApi = {
    contract: null,
    async getContract() {
        const single = await maskUtil.getSingle()
        if (single) {
            this.contract = new ethers.Contract(ContractAddressConfig.ERC721, ABI, single)
        }
        else {
            this.contract = new ethers.Contract(ContractAddressConfig.ERC721, ABI, maskUtil.getProvider())
        }
        return this.contract
    },
    async balanceOf(address) {
        const contract = await this.getContract()
        return await contract.balanceOf(address)
    },
}

export default nftContractApi;

